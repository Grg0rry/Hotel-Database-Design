CREATE TABLE branches
(
  Hotel_ID		CHAR(4) PRIMARY KEY,
  Location		VARCHAR(15) NOT NULL,
  Rating		NUMBER(1) CONSTRAINT check_rating
			CHECK (Rating BETWEEN 1 AND 5)
);

CREATE TABLE room_type
(
  Room_code		CHAR(2) PRIMARY KEY,
  Room_type		VARCHAR(10) NOT NULL,
  Room_rate		NUMBER(6,2) NOT NULL,
  Num_occupants		NUMBER(1) CONSTRAINT check_occupants
			CHECK (Num_occupants BETWEEN 4 AND 5)
);

CREATE TABLE room
(
  Room_num		CHAR(4) PRIMARY KEY,
  Hotel_ID		CHAR(4) NOT NULL,
  Room_code		CHAR(2) NOT NULL,
  Room_location		CHAR(5) NOT NULL,
FOREIGN KEY (Hotel_ID) REFERENCES BRANCHES(Hotel_ID),
FOREIGN KEY (Room_code) REFERENCES ROOM_TYPE(Room_code)
); 
 
CREATE TABLE members
(
  Member_ID         	CHAR(4) PRIMARY KEY,
  Firstname		VARCHAR(20) NOT NULL,
  Lastname		VARCHAR(20) NOT NULL,
  Gender              	CHAR(1) CONSTRAINT check_gender
  			CHECK (Gender IN ('M','F')),
  Identification_num 	VARCHAR(14) NOT NULL,
  Nationality		VARCHAR(20) NOT NULL,
  Email              	VARCHAR2(40) NOT NULL,
  Contact             	NUMBER(15) NOT NULL,
  Street		VARCHAR2(50) NOT NULL,
  City			VARCHAR(20) NOT NULL,
  Region		VARCHAR(20),
  Country		VARCHAR(20) NOT NULL,
  Postcode		VARCHAR(10) NOT NULL,  
  Birth_date	        DATE NOT NULL	
);

CREATE TABLE special_request
(
  Request_ID		CHAR(5) PRIMARY KEY,
  Request_name		VARCHAR2(40) NOT NULL,
  Charge		NUMBER(5,2) 
);

CREATE TABLE reservation
( 
  Reservation_ID	CHAR(5) PRIMARY KEY,
  Booking_date        	DATE NOT NULL,
  Member_ID         	CHAR(4) NOT NULL,
  Booking_time		NUMBER(4) NOT NULL,
  Checkin		DATE NOT NULL,		
  Checkout		DATE NOT NULL,
FOREIGN KEY (Member_ID) REFERENCES MEMBERS(Member_ID)
);

CREATE TABLE booking
(
  Booking_ID		CHAR(4) PRIMARY KEY,
  Actual_checkin	NUMBER(4),
  Actual_Checkout	NUMBER(4),
  Cancellation_date	DATE,
  Cancellation_reason   VARCHAR2(40),
  Receipt_ID		CHAR(5) NOT NULL
);

CREATE TABLE reservation_detail
( CONSTRAINT PK_reservation_detail PRIMARY KEY (Reservation_ID, Room_num),
  Reservation_ID	CHAR(5),
  Room_num		CHAR(4), 
  Request_ID     	CHAR(5),
  Booking_ID		CHAR(4) CONSTRAINT Booking_ID_u UNIQUE,
FOREIGN KEY (Reservation_ID) REFERENCES RESERVATION(Reservation_ID),
FOREIGN KEY (Room_num) REFERENCES ROOM(Room_num),
FOREIGN KEY (Booking_ID) REFERENCES BOOKING(Booking_ID),
FOREIGN KEY (Request_ID) REFERENCES SPECIAL_REQUEST(Request_ID)
);

CREATE TABLE inspection
( 
  Inspect_ID		CHAR(4) PRIMARY KEY,
  Room_num		CHAR(4) NOT NULL,
  Date_check		DATE NOT NULL,
  Condition		CHAR(1)	CONSTRAINT check_Condition
			CHECK (Condition IN ('S','E','G','P')),
FOREIGN KEY (Room_num) REFERENCES ROOM(Room_num)
);

CREATE TABLE contractor
(
  Contractor_ID		CHAR(4) PRIMARY KEY,
  Cont_name		VARCHAR2(40) NOT NULL,
  Company		VARCHAR2(40) NOT NULL,
  Cont_contact		NUMBER(15) NOT NULL
 );

CREATE TABLE maintenance
(
  Maintenance_ID	CHAR(5) PRIMARY KEY,
  Main_cost		NUMBER(8,2) NOT NULL,
  Main_date		DATE NOT NULL,
  Contractor_ID		CHAR(4) NOT NULL,
  Inspect_ID		CHAR(4) NOT NULL,
FOREIGN KEY (Contractor_ID) REFERENCES CONTRACTOR(Contractor_ID),
FOREIGN KEY (Inspect_ID) REFERENCES INSPECTION(Inspect_ID)
);

CREATE TABLE maintenance_tasks
( CONSTRAINT PK_tasks PRIMARY KEY (Maintenance_ID, Start_time),
  Maintenance_ID	CHAR(5),
  Start_time		NUMBER(4),
  Tasks			VARCHAR(20) NOT NULL,
  Repair_reason		VARCHAR2(100) NOT NULL,
FOREIGN KEY (Maintenance_ID) REFERENCES MAINTENANCE(Maintenance_ID)
);

